@dataclass
class QuestionOption:
    """A single option for a question."""

    id: str
    label: str
    value: int = 0  # Optional score/weight for the option


@dataclass
class Question:
    """A structured question with options."""

    id: str
    text: str
    type: str  # "single" or "multiple"
    category: str  # Question category for grouping
    options: list[QuestionOption] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        """Convert to dictionary."""
        return {
            "id": self.id,
            "text": self.text,
            "type": self.type,
            "category": self.category,
            "options": [{"id": opt.id, "label": opt.label} for opt in self.options],
        }


@dataclass
class Goal:
    """A predefined goal for a domain."""

    id: str
    label: str
    description: str
    domain: Domain

    def to_dict(self) -> dict[str, Any]:
        """Convert to dictionary."""
        return {
            "id": self.id,
            "label": self.label,
            "description": self.description,
        }


