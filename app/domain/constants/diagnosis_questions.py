from dataclasses import dataclass, field
from enum import Enum
from typing import Any


class Domain(str, Enum):
    """Available engineering domains."""

    FRONTEND = "frontend"
    BACKEND = "backend"
    INFRASTRUCTURE = "infrastructure"

    @property
    def display_name(self) -> str:
        """Get display name for the domain."""
        names = {
            Domain.FRONTEND: "フロントエンド",
            Domain.BACKEND: "バックエンド",
            Domain.INFRASTRUCTURE: "インフラ",
        }
        return names.get(self, self.value)

    @property
    def description(self) -> str:
        """Get description for the domain."""
        descriptions = {
            Domain.FRONTEND: "ユーザーインターフェースの開発。React、Vue、TypeScriptなど",
            Domain.BACKEND: "サーバーサイド開発。API設計、データベース、認証など",
            Domain.INFRASTRUCTURE: "クラウド・インフラ構築。AWS、Docker、CI/CDなど",
        }
        return descriptions.get(self, "")


